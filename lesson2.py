# User ဆီက နာမည်ကို တောင်းခြင်း
name = input("သင့်နာမည်ကို ရိုက်ထည့်ပါ: ")

# User ဆီက အသက်ကို တောင်းခြင်း (input က စာသားအဖြစ်ပဲ သိမ်းတာမို့ int() နဲ့ ကိန်းပြည့်ပြောင်းပေးရပါတယ်)
age = int(input("သင့်အသက်ကို ရိုက်ထည့်ပါ: "))

# သင်္ချာတွက်ချက်ခြင်း (နောက် ၅ နှစ်ဆိုတော့ + 5 ပေါင်းပါမယ်)
future_age = age + 5

# အဖြေကို ပြန်ထုတ်ပြခြင်း
print("-" * 20)
print("မင်္ဂလာပါ " + name + " ခင်ဗျာ။")
print("နောက် ၅ နှစ်ဆိုရင် သင့်အသက်က " + str(future_age) + " နှစ် ဖြစ်သွားပါလိမ့်မယ်။")
