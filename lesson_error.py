print("--- ဂဏန်းနှစ်ခု စားခြင်း အစီအစဉ် ---")

try:
    # User ဆီက ဂဏန်းတောင်းမယ်
    num1 = int(input("စားမည့်ကိန်း (Dividend) ရိုက်ပါ: "))
    num2 = int(input("စားမည့်ကိန်း (Divisor) ရိုက်ပါ: "))

    # စားခြင်းကို တွက်ချက်မယ်
    result = num1 / num2
    print(f"အဖြေမှာ: {result} ဖြစ်ပါတယ်။")

except ValueError:
    # User က ဂဏန်းမဟုတ်ဘဲ စာသားတွေ လျှောက်ရိုက်ရင် ဒီအလုပ်ကို လုပ်မယ်
    print("အမှားရှိပါသည်: ကျေးဇူးပြု၍ ဂဏန်းများကိုသာ ရိုက်ထည့်ပါ။")

except ZeroDivisionError:
    # သင်္ချာမှာ ၀ (Zero) နဲ့ စားလို့ မရတဲ့အတွက် အဲဒီအမှားကို ဖမ်းမယ်
    print("အမှားရှိပါသည်: ဂဏန်းတစ်ခုကို '၀' (Zero) နှင့် စား၍ မရပါ။")

except Exception as e:
    # တခြား မထင်မှတ်ထားတဲ့ အမှားတွေရှိရင် သိရအောင်
    print(f"မထင်မှတ်ထားသော အမှားတစ်ခု ဖြစ်ပေါ်ခဲ့သည်: {e}")

finally:
    # အမှားတက်သည်ဖြစ်စေ၊ မတက်သည်ဖြစ်စေ ဒီစာသားက အမြဲ ပေါ်လာပါမယ်
    print("အစီအစဉ်ကို အသုံးပြုသည့်အတွက် ကျေးဇူးတင်ပါသည်။")
