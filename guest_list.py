# ၁။ အစပိုင်းမှာ အလွတ်ဖြစ်နေတဲ့ List တစ်ခု တည်ဆောက်မယ်
guests = []

print("--- ဧည့်သည်စာရင်း မှတ်တမ်းတင်စနစ် ---")
print("(မှတ်တမ်းတင်တာ ရပ်ချင်ရင် 'stop' လို့ ရိုက်ပါ)\n")

# ၂။ နာမည်တွေကို အဆက်မပြတ် တောင်းယူဖို့ Loop ပတ်မယ်
while True:
    name = input("ဧည့်သည်နာမည် ရိုက်ထည့်ပါ: ")
    
    # 'stop' လို့ ရိုက်ရင် Loop ကို ရပ်လိုက်မယ်
    if name.lower() == 'stop':
        break
    
    # ရိုက်လိုက်တဲ့ နာမည်ကို List ထဲ ပေါင်းထည့်မယ်
    guests.append(name)
    print(f"{name} ကို စာရင်းထဲ ထည့်ပြီးပါပြီ။")

# ၃။ နောက်ဆုံးမှာ စာရင်းအားလုံးကို ပြန်ထုတ်ပြမယ်
print("\n" + "="*30)
print(f"ယနေ့ ကြွရောက်လာသော ဧည့်သည်စုစုပေါင်း: {len(guests)} ဦး")
print("="*30)

# List ထဲက နာမည်တွေကို အစဉ်လိုက် ထုတ်ပြခြင်း
for i in range(len(guests)):
    print(f"{i + 1}. {guests[i]}")

print("="*30)
