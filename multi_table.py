# User ဆီက ဘယ်အမြှောက်ဇယားကို သိချင်လဲဆိုတာ တောင်းပါမယ်
num = int(input("ဘယ်အမြှောက်ဇယားကို သိချင်ပါသလဲ? (ဥပမာ- 5): "))

print(f"\n--- {num} တလီ အမြှောက်ဇယား ---")

# ၁ ကနေ ၁၂ အထိ Loop ပတ်ပြီး တွက်ပါမယ်
for i in range(1, 13):
    result = num * i
    # ရလဒ်ကို ပုံစံတကျ ထုတ်ပြခြင်း
    print(str(num) + " x " + str(i) + " = " + str(result))

print("-" * 25)

