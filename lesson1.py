# ဒါကတော့ ပထမဆုံး သင်ခန်းစာပါ
# Variable ထဲမှာ အချက်အလက် သိမ်းဆည်းခြင်း

name = "Aung Aung"        # String (စာသား)
age = 20                   # Integer (ကိန်းပြည့်)
height = 5.6               # Float (ဒသမကိန်း)
is_student = True          # Boolean (ဟုတ်/မဟုတ်)

# သိမ်းထားတဲ့ အချက်အလက်တွေကို ပြန်ထုတ်ပြခြင်း
print("နာမည် -", name)
print("အသက် -", age)
print("အရပ်အမောင်း -", height)
print("ကျောင်းသား ဟုတ်/မဟုတ် -", is_student)
