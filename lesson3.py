# User ရဲ့ အသက်ကို တောင်းပါမယ်
age = int(input("သင့်အသက်ကို ရိုက်ထည့်ပါ: "))

# အခြေအနေကို စစ်ဆေးခြင်း (If-Else)
if age >= 18:
    print("ဂုဏ်ယူပါတယ်။ သင်ဟာ အရွယ်ရောက်ပြီးသူ ဖြစ်ပါတယ်။")
    print("ဒီ App ကို အသုံးပြုခွင့် ရှိပါတယ်။")
else:
    print("စိတ်မကောင်းပါဘူး။ သင်ဟာ အသက်မပြည့်သေးပါဘူး။")
    print("ဒီ App ကို သုံးဖို့ နောက်ထပ် " + str(18 - age) + " နှစ် စောင့်ရပါဦးမယ်။")

print("-" * 20)
print("အစီအစဉ် ပြီးဆုံးပါပြီ။")
