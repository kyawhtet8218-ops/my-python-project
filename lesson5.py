# ၁ ကနေ ၁၀ ထိ အလိုအလျောက် ရေတွက်ပြခြင်း
print("၁ ကနေ ၁၀ ထိ ရေတွက်ပြပါမယ် -")

# range(1, 11) ဆိုတာ ၁ ကနေ စပြီး ၁၁ မတိုင်ခင် (၁၀) ထိလို့ ဆိုလိုတာပါ
for i in range(1, 11):
    print("နံပါတ်: " + str(i))

print("-" * 20)

# နောက်ထပ် ဥပမာတစ်ခု (စာသားကို ၅ ကြိမ် ရိုက်ခိုင်းခြင်း)
text = "Coding ကို ချစ်မြတ်နိုးပါတယ်"
for count in range(5):
    print(str(count + 1) + ". " + text)
